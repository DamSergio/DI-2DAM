    @Override
    public void propertyChange(PropertyChangeEvent evt) {
       System.out.println("La tarea manda: "+evt.getPropertyName() +
               " con valor: " + evt.getNewValue());
       
       int progress = hilos.getProgress();
       String mensaje = "Completado: " + progress;
       progressMonitor.setNote(mensaje);
       progressMonitor.setProgress(progress);
       
       if (progressMonitor.isCanceled()){
            hilos.cancel(true);
            getContentPane().setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
            System.out.println("Se ha cancelado la tarea en curso");
        }
       
       if (evt.getPropertyName() == "state"){
           jProgressBar1.setIndeterminate(true);
       }
       
       if (evt.getPropertyName() == "progress"){
           jProgressBar1.setValue((int) evt.getNewValue());
           jProgressBar1.setIndeterminate(false);
       }
       
       if(evt.getNewValue() == SwingWorker.StateValue.DONE){
           try {
               System.out.println("Valor final: "+((HilosDeEjecuci贸n)evt.getSource()).get());
               getContentPane().setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));
               jButton1.setEnabled(true);
           } catch (InterruptedException ex) {
               Logger.getLogger(FramePrincipal.class.getName()).log(Level.SEVERE, null, ex);
           } catch (ExecutionException ex) {
               Logger.getLogger(FramePrincipal.class.getName()).log(Level.SEVERE, null, ex);
           } catch (CancellationException c){
               System.out.println("Se cancela");
               jButton1.setEnabled(true);
           }
       }
    }
