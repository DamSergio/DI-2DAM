    private void btnDesactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDesactActionPerformed
        // TODO add your handling code here:
        int userCode = (int) spCode.getValue();
        if (userCode == code){
            JOptionPane.showMessageDialog(this, "HAS GANADO", "GANASTES", JOptionPane.INFORMATION_MESSAGE);
            cd.cancel(true);
        }
        
        if (userCode > code){
            JOptionPane.showMessageDialog(this, "El codigo de la bomba es menor", "Fallastes", JOptionPane.INFORMATION_MESSAGE);
        }
        
        if (userCode < code){
            JOptionPane.showMessageDialog(this, "El codigo de la bomba es mayor", "Fallastes", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnDesactActionPerformed

