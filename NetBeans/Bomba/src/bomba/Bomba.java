    @Override
    public void propertyChange(PropertyChangeEvent evt) {
        if (evt.getPropertyName() == "progress"){
           pbBomb.setValue((int) evt.getNewValue());
           pbBomb.setIndeterminate(false);
           //System.out.println(progress);
        }
        
        if(evt.getNewValue() == SwingWorker.StateValue.DONE){
           try {
               btnDesact.setEnabled(false);
               btnStart.setEnabled(true);
               lbBomb.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/bomba2.PNG")));
               JOptionPane.showMessageDialog(this, "PERDISTES", "BOBO", JOptionPane.INFORMATION_MESSAGE);
           } catch (CancellationException c){
               btnStart.setEnabled(true);
               btnDesact.setEnabled(false);
           }
       }
    }
